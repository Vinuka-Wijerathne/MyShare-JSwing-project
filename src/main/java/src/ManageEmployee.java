    public void connectToDatabase() {
        try {
            String url = "jdbc:mysql://localhost:3306/mysharedb";
            String userName = "root";
            String password = "";

            con = DriverManager.getConnection(url, userName, password);
            statement = con.createStatement();
        } catch (SQLException sqlEx) {
            JOptionPane.showMessageDialog(rootPane, sqlEx);
        }
    }

    public void getDatatoTable() {  //gets data from the db
        try {
            String query = "SELECT * FROM Employee";

            ResultSet resultSet = statement.executeQuery(query);

            while (resultSet.next()) {
                int empID = resultSet.getInt("empID");
                String empName = resultSet.getString("name");
                String NIC = resultSet.getString("NIC");
                String gender = resultSet.getString("gender");
                String address = resultSet.getString("address");
                String password = resultSet.getString("password");
                String telephone = resultSet.getString("telephone");
                double salary = resultSet.getDouble("salary");
                String role = resultSet.getString("Role");

                model.addRow(new Object[]{empID, empName, NIC, gender, address, password, telephone, salary, role});
            }
        } catch (SQLException sqlex) {
            JOptionPane.showMessageDialog(rootPane, sqlex);
        }
    }

