    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        //TEMPORAY
        lblTotal1.setText("450");

        String cusID = lblID.getText();
        String cusName = txtName1.getText();
        String cusTP = txtTele1.getText();
        String cusEmail = txtEmail1.getText();

//        String empID = cmbEmpId.getSelectedItem().toString();

// Check if customer details are filled
        if (cusName.isEmpty() || cusTP.isEmpty() || cusEmail.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please fill in customer details");
            return; // Exit the method if any customer detail is empty
        }

// Check if at least one order type is selected
        boolean isAdvertisementSelected = cmbObjAdv.getSelectedItem() != null && !txtTargeAudince.getText().isEmpty();
        boolean isBannerSelected = cmdBannerSize.getSelectedItem() != null && (int) spinnerQuantityBanner.getValue() > 0;
        boolean isArtworkSelected = rdioGraphic1.isSelected() || rdioIllus1.isSelected() || rdioMotion1.isSelected();

        if (!isAdvertisementSelected && !isBannerSelected && !isArtworkSelected) {
            JOptionPane.showMessageDialog(rootPane, "Please select at least one order type");
            return; // Exit the method if no order type is selected
        }
// Advertisement details
        String adObjec = "";
        String adTargetAudience = "";
        String startDate = "";
        String endDate = "";

        if (isAdvertisementSelected) {
            adObjec = cmbObjAdv.getSelectedItem().toString();
            adTargetAudience = txtTargeAudince.getText();
            String strtDyStr = txtStrtDat.getText();
            int strtMnth = strtMonth.getMonth();
            String endDyStr = txtEndDay.getText();
            int endMnth = endMonth.getMonth();

            // Validate and parse the start date
            if (strtDyStr.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Please fill in the start date");
                return; // Exit the method if start date is empty
            }
            int strtDy = Integer.parseInt(strtDyStr);

            // Validate the start date based on the selected month
            int maxDayInMonth = 31; // Maximum days in a month (default to 31)
            if (strtMnth == 3 || strtMnth == 5 || strtMnth == 8 || strtMnth == 10) {
                maxDayInMonth = 30; // Months with 30 days
            } else if (strtMnth == 1) {
                maxDayInMonth = 28; // February (assuming non-leap year)
            }

            if (strtDy < 1 || strtDy > maxDayInMonth) {
                JOptionPane.showMessageDialog(rootPane, "Please enter a valid start day for the selected month");
                return; // Exit the method if start day is invalid
            }

            startDate = strtDy + " - " + strtMnth;

            // Validate and parse the end date
            if (endDyStr.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Please fill in the end date");
                return; // Exit the method if end date is empty
            }
            int endDy = Integer.parseInt(endDyStr);

            // Validate the end date based on the selected month
            maxDayInMonth = 31; // Maximum days in a month (default to 31)
            if (endMnth == 3 || endMnth == 5 || endMnth == 8 || endMnth == 10) {
                maxDayInMonth = 30; // Months with 30 days
            } else if (endMnth == 1) {
                maxDayInMonth = 28; // February (assuming non-leap year)
            }

            if (endDy < 1 || endDy > maxDayInMonth) {
                JOptionPane.showMessageDialog(rootPane, "Please enter a valid end day for the selected month");
                return; // Exit the method if end day is invalid
            }

            endDate = endDy + " - " + endMnth;

            // Check if any advertisement detail is empty
            if (adObjec.isEmpty() || adTargetAudience.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Please fill in all advertisement details");
                return; // Exit the method if any advertisement detail is empty
            }
        }

// Banner details
        String bannerSize = "";
        int banQuantity = 0;
        String bannerDesc = "";

        if (isBannerSelected) {
            bannerSize = cmdBannerSize.getSelectedItem().toString();
            banQuantity = (int) spinnerQuantityBanner.getValue();
            bannerDesc = txtBannerDesc.getText();

            // Check if any banner detail is empty
            if (bannerSize.isEmpty() || bannerDesc.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Please fill in all banner details");
                return; // Exit the method if any banner detail is empty
            }
        }

// Artwork details
        String artworkType = "";
        String artworkDesc = "";

        if (isArtworkSelected) {
            if (rdioGraphic1.isSelected()) {
                artworkType = "Graphic Design";
            } else if (rdioIllus1.isSelected()) {
                artworkType = "Illustration";
            } else if (rdioMotion1.isSelected()) {
                artworkType = "Motion Graphics";
            } else {
                JOptionPane.showMessageDialog(rootPane, "Please select an Artwork Type");
                return; // Exit the method if artwork type is not selected
            }
            artworkDesc = txtArtworkDesc1.getText();

            // Check if any artwork detail is empty
            if (artworkDesc.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Please fill in artwork description");
                return; // Exit the method if artwork description is empty
            }
        }

        double totPrice = Double.parseDouble(lblTotal1.getText());

        try {
            String query = "UPDATE orders SET CustomerName = '" + cusName + "', CustomerTelephone = '" + cusTP + "', CustomerEmail = '" + cusEmail + "', AdObjective = '" + adObjec + "', AdTargetAudience = '" + adTargetAudience + "', StartDate = '" + startDate + "', EndDate = '" + endDate + "', BannerSize = '" + bannerSize + "', Quantity = " + banQuantity + ", BannerDescription = '" + bannerDesc + "', ArtworkType = '" + artworkType + "', ArtworkDescription = '" + artworkDesc + "', TotalPrice = " + totPrice + " WHERE OrderID = " + cusID;
//            String query2 = "UPDATE employee SET status = 'Occupied' WHERE empID = " + empID;

//            statement.executeUpdate(query2);

            int rowsUpdated = statement.executeUpdate(query);
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(rootPane, "Data updated successfully in the table.");
                model.setRowCount(0);
                fetchDataFromDB();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Failed to update data in the table.");
            }
        } catch (SQLException sqlex) {
            JOptionPane.showMessageDialog(rootPane, sqlex);
        }


    }//GEN-LAST:event_btnUpdateActionPerformed

