    private void tblRegMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRegMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblReg.getSelectedRow();

        lblID.setText(model.getValueAt(selectedRow, 0).toString());
        txtName1.setText(tblReg.getValueAt(tblReg.getSelectedRow(), 1).toString());
        txtTele1.setText(tblReg.getValueAt(tblReg.getSelectedRow(), 2).toString());
        txtEmail1.setText(tblReg.getValueAt(tblReg.getSelectedRow(), 3).toString());

        String objValue = tblReg.getValueAt(tblReg.getSelectedRow(), 5).toString();

        int cmbObjIndex;
        switch (objValue) {
            case "Awareness":
                cmbObjIndex = 0;
                break;
            case "Marketing":
                cmbObjIndex = 1;
                break;
            case "Charity":
                cmbObjIndex = 2;
                break;
            default:
                cmbObjIndex = 0; // Set a default index if the value doesn't match any case
        }

        cmbObjAdv.setSelectedIndex(cmbObjIndex);

        txtTargeAudince.setText(model.getValueAt(selectedRow, 6).toString());

        // Get the values from the table
        String startDateValue = tblReg.getValueAt(tblReg.getSelectedRow(), 7).toString();
        String endDateValue = tblReg.getValueAt(tblReg.getSelectedRow(), 8).toString();

        if (!startDateValue.equals("") || !endDateValue.equals("")) {
// Split the start date value into day and month
            String[] startDateParts = startDateValue.split(" - ");
            String strtDyStr = startDateParts[0];
            int strtMnth = Integer.parseInt(startDateParts[1]);

// Split the end date value into day and month
            String[] endDateParts = endDateValue.split(" - ");
            String endDyStr = endDateParts[0];
            int endMnth = Integer.parseInt(endDateParts[1]);

// Set the values to the corresponding fields
            txtStrtDat.setText(strtDyStr);
            strtMonth.setMonth(strtMnth - 1); // Assuming month indices start from 1
            txtEndDay.setText(endDyStr);
            endMonth.setMonth(endMnth - 1); // Assuming month indices start from 1
        } else {
            txtStrtDat.setText("");
            txtEndDay.setText("");
        }
        String res = model.getValueAt(selectedRow, 12).toString();

        switch (res) {
            case "Graphic Design":
                rdioGraphic1.setSelected(true);
                break;
            case "Illustration":
                rdioIllus1.setSelected(true);
                break;
            case "Motion Graphics":
                rdioMotion1.setSelected(true);
                break;
        }
        txtArtworkDesc1.setText(model.getValueAt(selectedRow, 13).toString());

        String banSize = tblReg.getValueAt(selectedRow, 9).toString();

        int bannerSizeIndex = 0;
        switch (banSize) {
            case "1024*780":
                bannerSizeIndex = 0;
                break;
            case "1280*1980":
                bannerSizeIndex = 1;
                break;
            case "540*480":
                bannerSizeIndex = 2;
                break;
        }
        cmdBannerSize.setSelectedIndex(bannerSizeIndex);

        spinnerQuantityBanner.setValue(model.getValueAt(selectedRow, 10));
        txtBannerDesc.setText(model.getValueAt(selectedRow, 11).toString());


    }//GEN-LAST:event_tblRegMouseClicked

