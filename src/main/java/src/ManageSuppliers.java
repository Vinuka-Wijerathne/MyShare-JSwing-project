    private void tblSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSupplierMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblSupplier.getSelectedRow();

        txtName1.setText(model.getValueAt(selectedRow, 1).toString());
        txtTel1.setText(model.getValueAt(selectedRow, 2).toString());
        txtAddress1.setText(model.getValueAt(selectedRow, 3).toString());
        String gender = model.getValueAt(selectedRow, 4).toString();
        if (gender.equalsIgnoreCase("M")) {
            rdioM1.setSelected(true);
        } else if (gender.equalsIgnoreCase("F")) {
            rdioF1.setSelected(true);
        }
        txtEmail1.setText(model.getValueAt(selectedRow, 5).toString());
        txtOrder1.setText(model.getValueAt(selectedRow, 6).toString());
    }//GEN-LAST:event_tblSupplierMouseClicked

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        try {
            int selectedRow = tblSupplier.getSelectedRow();
            String supID = model.getValueAt(selectedRow, 0).toString();
            String queryDelete = "DELETE FROM supplier WHERE supplierID = " + supID;
            statement.executeUpdate(queryDelete);
            JOptionPane.showMessageDialog(rootPane, "record deleted");
            model.setRowCount(0);
            getDataToTable();
        } catch (SQLException exsql) {
            JOptionPane.showMessageDialog(rootPane, exsql);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblSupplier.getSelectedRow();
        
        String supID = model.getValueAt(selectedRow, 0).toString();
        
        String name = txtName1.getText();
        String telephone = txtTel1.getText();
        String address = txtAddress1.getText();
        String gender = "";
        if (rdioM1.isSelected()) {
            gender = "M";
        } else if (rdioF1.isSelected()) {
            gender = "F";
        } else {
            JOptionPane.showMessageDialog(rootPane, "Select a Gender");
        }

        String email = txtEmail1.getText();
        String orders = txtOrder1.getText();

        try {
            String queryUpdate = "UPDATE supplier SET name = '" + name + "', telephone = '" + telephone + "', address = '" + address + "', gender = '" + gender + "', email = '" + email + "', orders = '" + orders + "' WHERE supplierID = " + supID;
            statement.executeUpdate(queryUpdate);
            JOptionPane.showMessageDialog(rootPane, "record updated");
            model.setRowCount(0);
            getDataToTable();
        } catch (SQLException exsql) {
            JOptionPane.showMessageDialog(rootPane, exsql);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

