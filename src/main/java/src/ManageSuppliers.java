    public void getDataToTable() {
        String query = "SELECT * FROM supplier";
        try {
            ResultSet resultSet = statement.executeQuery(query); //saves the entire in the resultset
            
            while(resultSet.next()){
                int ID = resultSet.getInt("supplierID");  //vales from DB table
                String name =  resultSet.getString("name");
                String telephone = resultSet.getString("telephone");
                String address = resultSet.getString("address");
                String gender = resultSet.getString("gender");
                String email = resultSet.getString("email");
                String orders = resultSet.getString("orders");
                
                model.addRow(new Object[]{ID, name, telephone, address, gender, email, orders});  //sends values to UI table
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }
